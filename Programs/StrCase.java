import java.util.*;
public class StrCase{
	public static void main(String[] args){
		Scanner s = new Scanner(System.in);
		System.out.println("Enter the string");
		String p = s.nextLine();
		System.out.println("Str Converted to Uppercase is "+p.toUpperCase());
		System.out.println("Str Converted to Lowercase is "+p.toLowerCase());
	}
}	